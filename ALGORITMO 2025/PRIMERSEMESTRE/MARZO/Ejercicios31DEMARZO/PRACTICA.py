def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

num1 = int(input("Digita el primer número: "))
num2 = int(input("Digita el segundo número: "))
resp = suma(num1, num2)
print("La suma es:", resp)
resp = resta(num1, num2)
print("La resta es:", resp)
print("Ejemplificando funciones en Python. La Suma y Resta se realizo correctamente")
